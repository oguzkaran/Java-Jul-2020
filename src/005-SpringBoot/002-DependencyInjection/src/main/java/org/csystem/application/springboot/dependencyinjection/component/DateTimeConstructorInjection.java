package org.csystem.application.springboot.dependencyinjection.component;

import org.springframework.stereotype.Component;

import javax.annotation.PostConstruct;

@Component
public class DateTimeConstructorInjection {
    private final DateTime m_dateTime;

    public DateTimeConstructorInjection(DateTime dateTime)
    {
        m_dateTime = dateTime;
    }

    @PostConstruct
    public void displayDateTime()
    {
        System.out.printf("DateTimeConstructorInjection:%s%n", m_dateTime);
    }
}
